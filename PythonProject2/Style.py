def color(theme_name, color_name):
    """Return the hex code for the requested color from the specified theme."""
    theme = list_1.get(theme_name.lower())
    if theme:
        return theme["colors"].get(color_name.lower(), "#000000")
    return "#000000"  # default to black if theme not found



